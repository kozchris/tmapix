package org.tmapix.io;

/**
 * Provides constansts for XML Topic Maps (XTM) syntaxes.
 * 
 * @author Lars Heuer (heuer[at]semagia.com) <a href="http://www.semagia.com/">Semagia</a>
 * @version $Rev$ - $Date$
 */
public enum XTMVersion {

    /**
     * Represents <a href="http://www.topicmaps.org/xtm/1.0">XTM 1.0</a>
     */
    XTM_1_0,
    
    /**
     * Represents <a href="http://www.isotopicmaps.org/sam/sam-xtm/2006-06-19/">XTM 2.0</a>
     */
    XTM_2_0,
    
    /**
     * Represents <a href="http://www.itscj.ipsj.or.jp/sc34/open/1378.htm">XTM 2.1</a>
     */
    XTM_2_1;
}
